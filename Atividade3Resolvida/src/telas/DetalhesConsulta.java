public Detalhes getDetalhes(){
    Detalhes detalhes = new Detalhes ();
    
    detalhes.setConsultaRealizada("Não");
    detalhes.setConsultaSelecionada(1);
    detalhes.setReceitaEObservacao(txtObservacao.getText());
    
    
    
    
    
    
    return detalhes;
}
    public String consultaRealizada(){
        String resposta;
        if(getDetalhes().getConsultaRealizada().equals("")){
            resposta = "Não";
            
        }else{
            resposta = "jaRealizada";
        }
        
        return resposta;
    }
    public void mostrarObs(String mesagem){
        txtObservacao.setText(mesagem);
       }
    public void checkConsultaOff(){
        
        btnFinalizar.setVisible(false);
        checkConsultaRealizada.setSelected(true);
        checkConsultaRealizada.setEnabled(false);
        
    }
    public void checkConsultaOn(){
        
        btnFinalizar.setVisible(true);
        checkConsultaRealizada.setSelected(false);
        checkConsultaRealizada.setEnabled(true);
        
    }
    

    






