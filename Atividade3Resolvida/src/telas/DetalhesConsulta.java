    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
       //chama o método para finalizar, vindo a posicao da tela principal
       int posicao = TelaPrincipal.telaPrincipal.pegarPosicao();
       if(!getDetalhes().getReceitaEObservacao().equals("")){
       
       if(checkConsultaRealizada.isSelected()){
              
       TelaPrincipal.telaPrincipal.atualizarJaRealizada(consultaRealizada(), posicao);
       TelaPrincipal.telaPrincipal.guardarObs(getDetalhes().getReceitaEObservacao(), posicao);
       this.dispose();
       }else{
           TelaPrincipal.telaPrincipal.guardarObs(getDetalhes().getReceitaEObservacao(), posicao);
           this.dispose();
       }
       }else{
           JOptionPane.showMessageDialog(rootPane, "Observação não pode estar vazio ");

       }  
    }//GEN-LAST:event_btnFinalizarActionPerformed
   
